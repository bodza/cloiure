                        (§ ass pc (ß pc.clone()))
                        (ß pc[radix] = cacheLine)
                        (ß BigInteger'powerCache = pc) ;; volatile write, publish
                    )
                    (ß cacheLine[exponent])
                )
            )
        )
    )

