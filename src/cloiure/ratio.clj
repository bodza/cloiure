        (ß mag[0] &= (1 << (32 - excessBits)) - 1)

        (ß (mag[0] == 0) ? BigInteger'new(1, mag) #_":" BigInteger'new(mag, 1))
    )

    ;;;
     ; Returns a BigInteger whose value is {@code (this}<sup>-1</sup> {@code mod m)}.
     ;
     ; @param  m the modulus.
     ; @return {@code this}<sup>-1</sup> {@code mod m}.
