                    (let [
                          #_"int" j 0
                    ]
                        (while (ß j < magLen - 1)
                            (ß newMag[i] = mag[j] << nBits | mag[j + 1] >>> nBits2)
                            (§ ass i (ß i + 1))
                            (§ ass j (ß j + 1))
