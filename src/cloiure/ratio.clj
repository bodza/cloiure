    ;;;
     ; Returns a BigInteger whose value is {@code (this mod m}).
     ; This method differs from {@code remainder} in that it always returns
     ; a <i>non-negative</i> BigInteger.
     ;
     ; @param  m the modulus.
     ; @return {@code this mod m}
