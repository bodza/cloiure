                    (do
                        ;; use space on right
                        (loop-when-recur [_ (§ ass #_"int" i 0)] (ß i < newLen - this.intLen) [(§ ass i (ß i + 1))]
                            (ß this.value[this.offset + this.intLen + i] = 0)
