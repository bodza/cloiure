    ;;;
     ; Right shift this MutableBigInteger n bits, where n is less than 32.
     ; Assumes that intLen > 0 and n > 0 for speed.
     ;;
    #_method
    (defn- #_"void" MutableBigInteger''primitiveRightShift [#_"MutableBigInteger" this, #_"int" n]
        (let [
              #_"int[]" val (:value this)
              #_"int" n2 (- 32 n)
        ]
            (loop-when-recur [#_"int" i (dec (+ (:offset this) (:intLen this))) #_"int" c (aget val i)] (< (:offset this) i) [(dec i) c]
                (let [
                      #_"int" b c
                ]
                    (§ ass c (aget val (dec i)))
                    (§ ass (aget val i) (| (<< c n2) (>>> b n)))
                )
            )
            (§ ass (aget val (:offset this)) (>>> (aget val (:offset this)) n))
        )
        nil
    )

    ;;;
     ; Left shift this MutableBigInteger n bits, where n is less than 32.
     ; Assumes that intLen > 0 and n > 0 for speed.
     ;;
    #_method
    (defn- #_"void" MutableBigInteger''primitiveLeftShift [#_"MutableBigInteger" this, #_"int" n]
        (let [
              #_"int[]" val (:value this)
              #_"int" n2 (- 32 n)
        ]
            (loop-when-recur [#_"int" i (:offset this) #_"int" c (aget val i) #_"int" m (dec (+ i (:intLen this)))] (< i m) [(inc i) c m]
                (let [
                      #_"int" b c
                ]
                    (§ ass c (aget val (inc i)))
                    (§ ass (aget val i) (| (<< b n) (>>> c n2)))
                )
            )
            (§ ass (aget val (dec (+ (:offset this) (:intLen this)))) (<< (aget val (dec (+ (:offset this) (:intLen this)))) n))
        )
        nil
    )

    ;;;
     ; Adds the contents of two MutableBigInteger objects.
     ; The result is placed within this MutableBigInteger.
     ; The contents of the addend are not changed.
     ;;
    #_method
    (defn #_"void" MutableBigInteger''add [#_"MutableBigInteger" this, #_"MutableBigInteger" addend]
        (let [
              #_"int" x (:intLen this)
              #_"int" y (:intLen addend)
              #_"int" resultLen (max x y)
              #_"int[]" result (if (< (alength (:value this)) resultLen) (int-array resultLen) (:value this))
              #_"int" rstart (- (alength result) 1)
              #_"long" sum (ß )
              #_"long" carry 0

        ]
            ;; add common parts of both numbers
            (while (and (pos? x) (pos? y))
                (§ ass x (dec x))
                (§ ass y (dec y))
                (§ ass sum (+ (& (aget (:value this) (+ x (:offset this))) BigInteger'LONG_MASK) (& (aget (:value addend) (+ y (:offset addend))) BigInteger'LONG_MASK) carry))
                (§ ass (aget result rstart) (int sum))
                (§ ass rstart (dec rstart))
                (§ ass carry (>>> sum 32))
            )

            ;; add remainder of the longer number
            (while (pos? x)
                (let [
                ]
                    (§ ass x (dec x))
                    (when (and (zero? carry) (== result (:value this)) (== rstart (+ x (:offset this))))
                        (§ return nil)
                    )
                    (§ ass sum (+ (& (aget (:value this) (+ x (:offset this))) BigInteger'LONG_MASK) carry))
                    (§ ass (aget result rstart) (int sum))
                    (§ ass rstart (dec rstart))
                    (§ ass carry (>>> sum 32))
                )
            )
            (while (pos? y)
                (§ ass y (dec y))
                (§ ass sum (+ (& (aget (:value addend) (+ y (:offset addend))) BigInteger'LONG_MASK) carry))
                (§ ass (aget result rstart) (int sum))
                (§ ass rstart (dec rstart))
                (§ ass carry (>>> sum 32))
            )

            (when (pos? carry) ;; result must grow in length
                (let [
                ]
                    (§ ass resultLen (inc resultLen))
                    (cond (< (alength result) resultLen)
                        (let [
                              #_"int[]" temp (int-array resultLen)
                        ]
                            ;; result one word longer from carry-out,
                            ;; copy low-order bits into new result
                            (System/arraycopy result, 0, temp, 1, (alength result))
                            (§ ass (aget temp 0) 1)
                            (§ ass result temp)
                        )
                        :else
                        (do
                            (§ ass (aget result rstart) 1)
                            (§ ass rstart (dec rstart))
                        )
                    )
                )
            )

            (§ ass (:value this) result)
            (§ ass (:intLen this) resultLen)
            (§ ass (:offset this) (- (alength result) resultLen))
        )
        nil
    )

    ;;;
     ; Subtracts the smaller of this and b from the larger and places the
     ; result into this MutableBigInteger.
     ;;
    #_method
    (defn #_"int" MutableBigInteger''subtract [#_"MutableBigInteger" this, #_"MutableBigInteger" b]
        (let [
              #_"MutableBigInteger" a this
              #_"int[]" result (:value this)
              #_"int" sign (MutableBigInteger''compare a, b)
        ]
            (when (zero? sign)
                (MutableBigInteger''reset this)
                (§ return 0)
            )
            (when (neg? sign)
                (let [
                      #_"MutableBigInteger" tmp a
                ]
                    (§ ass a b)
                    (§ ass b tmp)
                )
            )

            (let [
                  #_"int" resultLen (:intLen a)
            ]
                (when (< (alength result) resultLen)
                    (§ ass result (int-array resultLen))
                )

                (let [
                      #_"long" diff 0
                      #_"int" x (:intLen a)
                      #_"int" y (:intLen b)
                      #_"int" rstart (- (alength result) 1)
                ]
                    ;; subtract common parts of both numbers
                    (while (pos? y)
                        (§ ass x (dec x))
                        (§ ass y (dec y))
                        (§ ass diff (- (& (aget (:value a) (+ x (:offset a))) BigInteger'LONG_MASK) (& (aget (:value b) (+ y (:offset b))) BigInteger'LONG_MASK) (int (- (>> diff 32)))))
                        (§ ass (aget result rstart) (int diff))
                        (§ ass rstart (dec rstart))
                    )
                    ;; subtract remainder of longer number
                    (while (pos? x)
                        (§ ass x (dec x))
                        (§ ass diff (- (& (aget (:value a) (+ x (:offset a))) BigInteger'LONG_MASK) (int (- (>> diff 32)))))
                        (§ ass (aget result rstart) (int diff))
                        (§ ass rstart (dec rstart))
                    )

                    (§ ass (:value this) result)
                    (§ ass (:intLen this) resultLen)
                    (§ ass (:offset this) (- (alength (:value this)) resultLen))
                    (MutableBigInteger''normalize this)
                    sign
                )
            )
        )
    )

    ;;;
     ; Subtracts the smaller of a and b from the larger and places the result into the larger.
     ; Returns 1 if the answer is in a, -1 if in b, 0 if no operation was performed.
     ;;
    #_method
    (defn- #_"int" MutableBigInteger''difference [#_"MutableBigInteger" this, #_"MutableBigInteger" b]
        (let [
              #_"MutableBigInteger" a this
              #_"int" sign (MutableBigInteger''compare a, b)
        ]
            (when (zero? sign)
                (§ return 0)
            )
            (when (neg? sign)
                (let [
                      #_"MutableBigInteger" tmp a
                ]
                    (§ ass a b)
                    (§ ass b tmp)
                )
            )

            (let [
                  #_"long" diff 0
                  #_"int" x (:intLen a)
                  #_"int" y (:intLen b)
            ]
                ;; subtract common parts of both numbers
                (while (pos? y)
                    (§ ass x (dec x))
                    (§ ass y (dec y))
                    (§ ass diff (- (& (aget (:value a) (+ (:offset a) x)) BigInteger'LONG_MASK) (& (aget (:value b) (+ (:offset b) y)) BigInteger'LONG_MASK) (int (- (>> diff 32)))))
                    (§ ass (aget (:value a) (+ (:offset a) x)) (int diff))
                )
                ;; subtract remainder of longer number
                (while (pos? x)
                    (§ ass x (dec x))
                    (§ ass diff (- (& (aget (:value a) (+ (:offset a) x)) BigInteger'LONG_MASK) (int (- (>> diff 32)))))
                    (§ ass (aget (:value a) (+ (:offset a) x)) (int diff))
                )

                (MutableBigInteger''normalize a)
                sign
            )
        )
    )

    ;;;
     ; Multiply the contents of two MutableBigInteger objects. The result is
     ; placed into MutableBigInteger z. The contents of y are not changed.
     ;;
    #_method
    (defn #_"void" MutableBigInteger''multiply [#_"MutableBigInteger" this, #_"MutableBigInteger" y, #_"MutableBigInteger" z]
        (let [
              #_"int" xLen (:intLen this)
              #_"int" yLen (:intLen y)
              #_"int" newLen (+ xLen yLen)
        ]
            ;; put z into an appropriate state to receive product
            (when (< (alength (:value z)) newLen)
                (§ ass (:value z) (int-array newLen))
            )
            (§ ass (:offset z) 0)
            (§ ass (:intLen z) newLen)

            ;; the first iteration is hoisted out of the loop to avoid extra add
            (let [
                  #_"long" carry 0
            ]
                (loop-when-recur [#_"int" j (dec yLen) #_"int" k (dec (+ yLen xLen))] (<= 0 j) [(dec j) (dec k)]
                    (let [
                          #_"long" product (+ (* (& (aget (:value y) (+ j (:offset y))) BigInteger'LONG_MASK) (& (aget (:value this) (+ (dec xLen) (:offset this))) BigInteger'LONG_MASK)) carry)
                    ]
                        (§ ass (aget (:value z) k) (int product))
                        (§ ass carry (>>> product 32))
                    )
                )
                (§ ass (aget (:value z) (dec xLen)) (int carry))

                ;; perform the multiplication word by word
                (loop-when-recur [#_"int" i (- xLen 2)] (<= 0 i) [(dec i)]
                    (let [
                    ]
                        (§ ass carry 0)
                        (loop-when-recur [#_"int" j (dec yLen) #_"int" k (+ yLen i)] (<= 0 j) [(dec j) (dec k)]
                            (let [
                                  #_"long" product (+ (* (& (aget (:value y) (+ j (:offset y))) BigInteger'LONG_MASK) (& (aget (:value this) (+ i (:offset this))) BigInteger'LONG_MASK)) (& (aget (:value z) k) BigInteger'LONG_MASK) carry)
                            ]
                                (§ ass (aget (:value z) k) (int product))
                                (§ ass carry (>>> product 32))
                            )
                        )
                        (§ ass (aget (:value z) i) (int carry))
                    )
                )

                ;; remove leading zeros from product
                (MutableBigInteger''normalize z)
            )
        )
        nil
    )

    ;;;
     ; Multiply the contents of this MutableBigInteger by the word y.
     ; The result is placed into z.
     ;;
    #_method
    (defn #_"void" MutableBigInteger''mul [#_"MutableBigInteger" this, #_"int" y, #_"MutableBigInteger" z]
        (when (== y 1)
            (MutableBigInteger''copyValue-m z, this)
            (§ return nil)
        )

        (when (zero? y)
            (MutableBigInteger''clear z)
            (§ return nil)
        )

        ;; perform the multiplication word by word
        (let [
              #_"long" ylong (& y BigInteger'LONG_MASK)
              #_"int[]" zval (if (< (alength (:value z)) (inc (:intLen this))) (int-array (inc (:intLen this))) (:value z))
              #_"long" carry 0
        ]
            (loop-when-recur [#_"int" i (- (:intLen this) 1)] (<= 0 i) [(dec i)]
                (let [
                      #_"long" product (+ (* ylong (& (aget (:value this) (+ i (:offset this))) BigInteger'LONG_MASK)) carry)
                ]
                    (§ ass (aget zval (inc i)) (int product))
                    (§ ass carry (>>> product 32))
                )
            )

            (cond (zero? carry)
                (do
                    (§ ass (:offset z) 1)
                    (§ ass (:intLen z) (:intLen this))
                )
                :else
                (do
                    (§ ass (:offset z) 0)
                    (§ ass (:intLen z) (inc (:intLen this)))
                    (§ ass (aget zval 0) (int carry))
                )
            )
            (§ ass (:value z) zval)
            nil
        )
    )

