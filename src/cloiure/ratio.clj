            )

            (let [
                  #_"int" extraByte (ß (k == byteLength) ? 1 #_":" 0)
                  #_"int" intLength (ß ((byteLength - keep + extraByte) + 3) >>> 2)
                  #_"int[]" result (ß new int[intLength])
            ]
